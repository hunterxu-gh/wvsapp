
namespace AesKey {
	
    /*
	  Custom AES key, it is used by all servers.
	
	*/
	int aesKey[32] = {
		0x15, 0x00, 0x00, 0x00,
		0x25, 0x00, 0x00, 0x00,
		0x35, 0x00, 0x00, 0x00,
		0x45, 0x00, 0x00, 0x00,
		0x55, 0x00, 0x00, 0x00,
		0x65, 0x00, 0x00, 0x00,
		0x75, 0x00, 0x00, 0x00,
		0x85, 0x00, 0x00, 0x00
	};
	/*
	  WvsClient uses a XOR version to hide the aes key.
	*/
	int aesKey_with_xor[32] = {
		aesKey[0]  ^ 1, 0xFF, 0xFF, 0xFF,
		aesKey[4]  ^ 1, 0xFF, 0xFF, 0xFF,
		aesKey[8]  ^ 1, 0xFF, 0xFF, 0xFF,
		aesKey[12] ^ 1, 0xFF, 0xFF, 0xFF,
		aesKey[16] ^ 1, 0xFF, 0xFF, 0xFF,
		aesKey[20] ^ 1, 0xFF, 0xFF, 0xFF,
		aesKey[24] ^ 1, 0xFF, 0xFF, 0xFF,
		aesKey[28] ^ 1, 0xFF, 0xFF, 0xFF
	};

	void setUpAesKey(int *aesKeyLocation){
		for (auto i = 0; i < sizeof(aesKey) / sizeof(int); i++)
			*(aesKeyLocation + i) = aesKey[i];
	}
}